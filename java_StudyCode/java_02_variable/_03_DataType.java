// data type 数据类型


// java是强类型语言，每种数据类型在内存中分配不同大小的空间(字节)


// java数据类型：
// 	【1】.基本数据类型
// 		(1).数值型
// 			整数类型：
// 				byte [1]
// 				short [2]
// 				int [4]
// 				long [8]
// 			浮点类型：
// 				float [4]
// 				double [8] 
// 		(2).字符型：
// 			char [2] （可存放汉字：'国'）
// 		(3).布尔型：
// 			boolean [1] （true、false）
// 
//	 【2】.引用数据类型
//	 	(1).类（class）
//	 	(2).接口（interface）
//	 	(3).数组（[]）
//
//
// 上图说明：
// java数据类型分为两大类：基本类型 和 引用类型
// 基本类型有：数值型[byte,short,int,long,float,double]、char、boolean
// 引用类型有：类、接口、数组
//
//
// 注意：1.java没有无符号类型
//      2.字符串String类型属于类

