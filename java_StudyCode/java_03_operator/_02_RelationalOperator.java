// Relational operator： 关系运算符


// 关系运算符(比较运算符)：
// 关系运算符的结果都是boolean型
// 
// ==
// !=
// <
// >
// <=
// >=
// instanceof
// 
// 
// 注意事项：
// 	instanceof：判断是否是类的对象
//	语法： 对象 instanceof 类



public class _02_RelationalOperator{
	public static void main(String[] args) {
		
		System.out.println(7 < 8);// true
		
		boolean flag = 3 > 4;
		System.out.println(flag);// false
	}
}