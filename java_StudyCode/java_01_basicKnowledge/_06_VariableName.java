// java标识符命名规范：
// 
// 1.包名：多单词组成时字母都是小写
// 	   如：aaa.bbb.ccc
// 	   
// 2.类名、接口名：多单词组成时，所有单词首字母大写 【大驼峰】
//     如：TankShotGame
//     
// 3.变量名、方法名：第一个单词首字母小写，其他单词首字母大写 【小驼峰】
//     如：tankRun
//     
// 4.常量名：所有字母都大写，单词间用_分隔
