// IDE(集成开发环境) - IDEA的使用


// 1.IDEA中的编译和运行：
//  run：.java文件先会先被编译成.class文件，然后运行
//  src目录：存放.java文件
//  out目录：存放.class文件



// 2.IDEA常用快捷键
//
//  ctrl + y ：删除当前行
//  ctrl + d ：复制当前行
//  alt  + / ：补全代码
//  ctrl + / ： 添加或取消//
//  ctrl + alt + l ： 自动缩进代码格式
//
//  alt + enter ：自动导入需要的类
//  alt + insert ： 生成构造器
//  ctrl + h ：查看类的继承关系
//  ctrl + b ：光标移动到方法的定义
//  .var ：自动分配变量名
//  ......



// 3.模板(template)
// 查看模板：settings(设置) -> Editor(编辑) -> Live templates(活模板)
//
// 常见模板：
//  main：main方法
//  fori：for循环
//  sout：System.out.println()
//  ......